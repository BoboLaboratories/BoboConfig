package net.bobolabs.config;

import org.jetbrains.annotations.NotNull;


/**
 * An exception that is raised when specific situations emerge when interacting with configurations in bad ways.
 *
 * @since 2.0.0
 */
public class ConfigurationException extends RuntimeException {

    ConfigurationException(@NotNull String message) {
        super(message);
    }

    public ConfigurationException(@NotNull String message, @NotNull Throwable cause) {
        super(message, cause);
    }

}
